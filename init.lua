-- Function to expand all nodes
local function expand_all()
  require('neo-tree').navigate("filesystem", "/")
  vim.cmd("normal! zR")
end

-- Key mapping for expanding all nodes
vim.api.nvim_set_keymap(
  'n', 
  '<leader>e', 
  ':lua expand_all()<CR>', 
  { noremap = true, silent = true }
)

